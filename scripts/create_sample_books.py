def create_books():
    for title, info in books.items():
        book_dir = os.path.join(BOOKS_DIR, title)
        os.makedirs(book_dir, exist_ok=True)
        for idx, (ch_title, ch_content) in enumerate(info['chapters'], 1):
            ch_filename = f'第{idx}章 {ch_title}.txt'
            ch_path = os.path.join(book_dir, ch_filename)
            with open(ch_path, 'w', encoding='utf-8') as f:
                f.write(f'{ch_title}\n\n{ch_content}')
        print(f'已创建: {title} ({len(info["chapters"])}章, 作者: {info["author"]}, 分类: {info["category"]})')

if __name__ == '__main__':
    create_books()
